    def _initialize_tiles(self, tile_size):
        """Create visual tile representations from world data."""
        for i, row in enumerate(self.data):
            for j, tile in enumerate(row):
                if tile in self.tile_images:
                    img = pygame.transform.scale(
                        self.tile_images[tile], 
                        (tile_size, tile_size)
                    )
                    self._add_tile_to_map(img, tile_size, i, j)
    
    def _initialize_collision_rects(self, tile_size):
        """
        Create optimized collision rectangles by merging adjacent solid tiles.
        Only tiles with value != 0 are considered solid.
        """
        checked = [[False for _ in row] for row in self.data]
        
        for i in range(len(self.data)):
            for j in range(len(self.data[i])):
                if self.data[i][j] == 0:
                    checked[i][j] = True
                if not checked[i][j]:
                    rect = self._find_contiguous_area(tile_size, i, j, checked)
                    self.rects.append(rect)
    
    def _find_contiguous_area(self, tile_size, start_i, start_j, checked):
        """
        Find the largest contiguous rectangular area of solid tiles.
        
        Returns:
            pygame.Rect: A rectangle covering the contiguous area
        """
        max_i, max_j = len(self.data), len(self.data[0])
        j = start_j
        width = 0
        
        # Calculate width of contiguous area in current row
        while j < max_j and self.data[start_i][j] != 0 and not checked[start_i][j]:
            j += 1
            width += 1
        
        # Calculate maximum height where width remains consistent
        height = 0
        for row in range(start_i, max_i):
            for col in range(start_j, start_j + width):
                if self.data[row][col] == 0 or checked[row][col]:
                    return pygame.Rect(
                        start_j * tile_size, 
                        start_i * tile_size, 
                        width * tile_size, 
                        height * tile_size)
                checked[row][col] = True
            height += 1
        
        return pygame.Rect(
            start_j * tile_size, 
            start_i * tile_size, 
            width * tile_size, 
            height * tile_size)
    
    def _add_tile_to_map(self, img, tile_size, i, j):
        """Add a tile image to the render list."""
        x_coord = j * tile_size
        y_coord = i * tile_size
        self.img_list.append((img, (x_coord, y_coord)))
    
    def display(self, cam_offset_x, cam_offset_y):
        """Render the world with camera offset."""
        self.screen.fill((20, 152, 220))  # Sky blue background
        
        for img, (x, y) in self.img_list:
            self.screen.blit(img, (x - cam_offset_x, y - cam_offset_y))